package weibo.model;

/**
 * 微博转发页面的信息解析反序列化模型
 * @author coderwang
 * 2014/2/28
 * */

public class WeiboAndRepost {
	
	String css;
	String js;
	String html;
	String domid;
	String ns;
	
	public String getCss() {
		return css;
	}
	public void setCss(String css) {
		this.css = css;
	}
	public String getJs() {
		return js;
	}
	public void setJs(String js) {
		this.js = js;
	}
	public String getHtml() {
		return html;
	}
	public void setHtml(String html) {
		this.html = html;
	}
	public String getDomid() {
		return domid;
	}
	public void setDomid(String domid) {
		this.domid = domid;
	}
	public String getNs() {
		return ns;
	}
	public void setNs(String ns) {
		this.ns = ns;
	}
	
	
	
}
